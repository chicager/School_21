#include "libft.h"

char		*ft_strtrim(char const *s1, char const *set)
{
	char	*dest;
	int		i;

	if (!s1)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	i = ft_strlen(s1);
	while (i >= 0 && ft_strchr(set, s1[i]))
		i--;
	dest = ft_substr(s1, 0, i + 1);
	return (dest);
}
