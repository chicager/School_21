#include "../minishell.h"

void	ft_pwd(void)
{
	char *ptr;

	ptr = NULL;
	ft_putstr_fd((ptr = getcwd(NULL, 0)), 1);
	write(1, "\n", 1);
	free(ptr);
	ptr = NULL;
}
